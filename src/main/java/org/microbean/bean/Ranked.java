package org.microbean.bean;

public interface Ranked {

  public static final int DEFAULT_RANK = 0;

  // Highest rank wins.
  public default int rank() {
    return DEFAULT_RANK;
  }

  public default boolean outranks(final Ranked other) {
    return other == null || this.rank() > other.rank();
  }

}
